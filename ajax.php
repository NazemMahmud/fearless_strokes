    function setCookie(tp, pr, nm, im, ex) {

        var d = new Date();
        d.setTime(d.getTime() + (ex * 24 * 60 * 60 * 1000));
        var expires = 'expires=' + d.toUTCString();

        for (var i = 0; i < products.length; i++) {
            if (products[i] === pr) {
                isNew = false;
                break;
            }
        }

        if (isNew && pr != 'null' && pr != '') {

            if (products.length == 11) {
                tps.splice(0,1);
                products.splice(0,1);
                names.splice(0,1);
                rImg.splice(0,1);
            }

            tps[tps.length] = tp;
            products[products.length] = pr;
            if(nm.length > 23)
                names[names.length] = nm.trim().replace(/[^\w\s]/gi, '').substring(0, 23) + '...';
            else
                names[names.length] = nm.trim().replace(/[^\w\s]/gi, '');
            rImg[rImg.length] = im;

            document.cookie = 'recentlyViewed=rokomari.com&tps=' + tps + '&pr=' + products + '&nm=' + names + '&im=' + rImg + '&;' + expires + ';path=/';
        }

    }

    function getCookie(cname) {
        var name = cname + '=';
        var ca = document.cookie.split('&');
        for (var i = 0; i < ca.length; i++) {
            var c = ca[i];
            while (c.charAt(0) == ' ') c = c.substring(1);
            if (c.indexOf(name) == 0) {
                return c.substring(name.length, c.length);
            }
        }
        return '';
    }

